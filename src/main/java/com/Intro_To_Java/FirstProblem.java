package com.Intro_To_Java;

public class FirstProblem {
    public static void main(String[] args) {

    //Requirement :  Calculate the sum of the first 100 numbers higher than 0

    int sum = 0;

    int n  = 100;

    for (short i = 1; i <=n ; i++) {

        sum = sum + i;

    }

    System.out.println("The sum of the first 100 numbers higher than 0 is: " + sum + "  :D");
}
}

